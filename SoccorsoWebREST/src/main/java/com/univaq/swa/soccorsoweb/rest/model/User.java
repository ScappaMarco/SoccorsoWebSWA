package com.univaq.swa.soccorsoweb.rest.model;

public class User {
    private String nome;
    private String email;

    public String getNome() {
        return this.nome;
    }

    public void setName(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return this.email;
    }

    public void setEmail(String email) {
        this.email = email;
    }   
}